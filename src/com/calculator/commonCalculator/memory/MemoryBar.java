    public void memoryStore() {
        memoryPanel.newMemory(new MemoryCell(numberPanel.getNumber()) {
            @Override
            public void memoryItemClicked(CNumber num) {
                numberPanel.setNumber(num);
            }
        });
    }

    public void memoryRecall() {
        numberPanel.setNumber(MemoryPanel.cells.get(0).getValue());
    }

    public void memoryAdd() {
        MemoryPanel.cells.get(0).addValue(numberPanel.getNumber());
    }

    public void memorySubstract() {
        MemoryPanel.cells.get(0).substractValue(numberPanel.getNumber());
    }

    public void clearAllMemory() {
        memoryPanel.clearAllMemory();
    }

